#Relational operators

age = int(input("Enter your age\n"))
if(age == 18):
    print("You're 18, Congratulations!")
else:
    print("You're not 18.")

age1 = int(input("Enter your age\n"))
if(age1 >= 18):
    print("You're 18, Congratulations!")
else:
    print("You're not 18 yet.")

